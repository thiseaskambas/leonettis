export type AdminLang = keyof typeof adminT;
export type AdminTranslations = (typeof adminT)[AdminLang];

/** Map known English API error strings to translation keys. */
const API_ERROR_MAP: Record<
  string,
  keyof (typeof adminT)['en']['form']['errors']
> = {
  'Failed to save listing': 'failedToSave',
  'slug cannot be empty': 'slugInvalid',
  'slug must contain at least one URL-safe character after normalization':
    'slugInvalid',
  'slug already exists': 'slugDuplicate',
  'Translation failed': 'translationFailed',
  'Improvement failed': 'improvementFailed',
  'Media upload failed': 'mediaUploadFailed',
  'Cannot delete media without a storage key': 'mediaDeleteNoKey',
  'Failed to delete media': 'mediaDeleteFailed',
  'Failed to save image order': 'imageOrderFailed',
  'Image upload failed': 'imageUploadFailed',
  'Image upload response missing media': 'imageUploadMissing',
  'Failed to prepare direct media upload': 'prepareUploadFailed',
  'Invalid presign response': 'invalidPresign',
  'Direct media upload failed': 'directUploadFailed',
  'Failed to finalize uploaded media': 'finalizeFailed',
  'Finalize response is missing media metadata': 'finalizeMissing',
  'Translation response is invalid': 'translationInvalid',
  'Empty response from improve API': 'improvementEmpty',
  'Missing required fields: title.en, listingType, propertyTypes':
    'propertyTypeRequired',
};

export function translateApiError(
  t: AdminTranslations,
  message: string | undefined
): string {
  if (!message) return t.form.errors.generic;
  const key = API_ERROR_MAP[message];
  return key ? t.form.errors[key] : message;
}
